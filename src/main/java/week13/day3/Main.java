        System.out.println(numbers.remove());
        System.out.println(numbers.remove());

        Queue<Integer> numbersOther = new PriorityQueue<>(Arrays.asList(2,4,3,1));
        Queue<Integer> numbersOther2 = new PriorityQueue<>(Arrays.asList(2,4,1,3));

